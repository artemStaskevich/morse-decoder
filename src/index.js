const MORSE_TABLE = {
    '.-':     'a',
    '-...':   'b',
    '-.-.':   'c',
    '-..':    'd',
    '.':      'e',
    '..-.':   'f',
    '--.':    'g',
    '....':   'h',
    '..':     'i',
    '.---':   'j',
    '-.-':    'k',
    '.-..':   'l',
    '--':     'm',
    '-.':     'n',
    '---':    'o',
    '.--.':   'p',
    '--.-':   'q',
    '.-.':    'r',
    '...':    's',
    '-':      't',
    '..-':    'u',
    '...-':   'v',
    '.--':    'w',
    '-..-':   'x',
    '-.--':   'y',
    '--..':   'z',
    '.----':  '1',
    '..---':  '2',
    '...--':  '3',
    '....-':  '4',
    '.....':  '5',
    '-....':  '6',
    '--...':  '7',
    '---..':  '8',
    '----.':  '9',
    '-----':  '0',
};

function decode(expr) {
    var res = [];
    for(var i=0; i < expr.length; i+=10 ) {
        var letter = expr.slice(i,i+10),
            morseLetter = letter.replace(/00/g, '').replace(/10/g, '.').replace(/11/g, '-');

        if (MORSE_TABLE[morseLetter]) {
            res.push(MORSE_TABLE[morseLetter]);
        } else {
            res.push(' ');
        }
    }

    return res.join('');
}

module.exports = {
    decode
}